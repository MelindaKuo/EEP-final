//Scrolling month function
var months = $(".months")
$(window).scroll(function() {
  if ($(window).width() > 613) {
    months.hide()
  } else {
    var height = $(document).scrollTop();
    if (height < 180) {
      months.hide();
    } else {
      months.show();
    }
    if (height < 500) {
      months.text("Sept")
    } else if (height < 1400) {
      months.text("Oct")
    } else if (height < 2400) {
      months.text("Nov")
    } else if (height < 3200) {
      months.text("Dec")
    } else if (height < 3450) {
      months.text("Jan")
    } else if (height < 3700) {
      months.text("Feb")
    } else if (height < 4600) {
      months.text("Mar")
    } else if (height < 4800) {
      months.text("Apr")
    };
    // $(".months").stop().animate({"marginTop": ($(window).scrollTop()/17) + "px"}, "fast" );
    clearTimeout($.data(this, 'scrollTimer'));
    $.data(this, 'scrollTimer', setTimeout(function() {
      months.fadeOut(600)
    }, 300));
  }


});
